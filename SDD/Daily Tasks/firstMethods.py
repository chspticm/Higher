# Methods
# A method is a function associated with an object.
# in Python these are appended to the object with a dot
# i.e Object.method()

name="Bob"

# .ljust() method
print('Name with spaces for columns',name.ljust(10),'left aligned')
print('Name with spaces for columns',name.rjust(10),'right aligned')

# .upper() method
print('Displays the contents in capitals',name.upper())
# .lower() method
print('Displays the contents in lower case',name.lower())
